class Person {
    String name;
    int age;
    String eyeColor;

    public Person() {
       this.name = "Alex White";
       this.age = 23;
       this.eyeColor = "Red"; 
    }

    public Person(String name, int age, String eyeColor) {
        this.name = name;
        this.age = age;
        this.eyeColor = eyeColor;
    }
}